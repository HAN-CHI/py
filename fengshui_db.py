# 六十甲子完整序列 (這可以用程式產生，也可以直接存入備查)
def generate_sixty_ganzhi():
    gz_list = []
    for i in range(60):
        stem = STEMS[i % 10]
        branch = BRANCHES[i % 12]
        gz_list.append(stem + branch)
    return gz_list

